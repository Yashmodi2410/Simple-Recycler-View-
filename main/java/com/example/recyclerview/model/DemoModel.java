package com.example.recyclerview.model;

public class DemoModel {


    String player_name;
    String player_run;
    String player_avg;
    int player_img;

    public String getPlayer_name() {
        return player_name;
    }

    public void setPlayer_name(String player_name) {
        this.player_name = player_name;
    }

    public String getPlayer_run() {
        return player_run;
    }

    public void setPlayer_run(String player_run) {
        this.player_run = player_run;
    }

    public String getPlayer_avg() {
        return player_avg;
    }

    public void setPlayer_avg(String player_avg) {
        this.player_avg = player_avg;
    }

    public int getPlayer_img() {
        return player_img;
    }

    public void setPlayer_img(int player_img) {
        this.player_img = player_img;
    }
}
